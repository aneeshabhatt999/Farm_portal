            <h2>What does the process of soil testing involve?</h2>
            <p><ol>Soil testing is divided into four sections - Soil Physics, Soil Chemistry and Fertility, Soil Biology and Environmental Soil Science.</ol>

                <ol>Soil Physics tests the geometrical arrangement or pattern of soil components like soil particles, soil aggregates, water, gas and solute in the soil. Equipment such as pressure plate apparatus, sand box, neutron moisture meter, depth density gauge, K-permeameter, rainfall simulator and root scanner are used. Testers come up with recommendations to enhance soil quality like conservation tillage, crop diversification, rainwater harvesting and sequestration.</ol>
                <ol>Soil Chemistry consists of testing the chemical characteristics for mineral composition, organic matter, PKN nutrients and Ph levels. Devices such as UV Visible Spectrophotometers, Deep Freezers, Sonicators, microwave digestion units and flame photometers are used. Such studies help in improving efficiency of nutrient cycling, minimizing nutrient losses from soils and improving soil quality for sustained high productivity.</ol>
                <ol> Soil Biology - is the study of microbial activity and ecology in the soil of organisms such as earthworms, nematodes, protozoa, fungi and bacteria. Laboratory tools such as laminar flow, BOD incubator and a research microscope with camera and CCTV system are used to view inner layers of the soil. Knowledge of soil biology can help in choosing fertilizers, pesticides and composting systems.</ol>
                <ol>Environmental Soil Science - deals with the extent of soil pollution due to city, industrial and agricultural wastes and indiscriminate use of fertilizers for crop production. It develops suitable prescriptions and technologies for safe recycling of these wastes into agriculturally productive soils.</ol></p>
            
